#include <iostream>

// Рекурсивная функция Аккермана
int ackermann(int m, int n) {
    // Базовые случаи для функции Аккермана
    if (m == 0) return n + 1;
    if (m == 1) return n + 2;
    if (m == 2) return 2 * n + 3;
    if (m == 3) return 2 * n + 4; // Этот случай проиллюстрирован для простоты

    // Рекурсивный вызов для остальных случаев
    return ackermann(m - 1, ackermann(m, n - 1));
}

int main() {
    int m, n;
    std::cout << "Введите два неотрицательных числа m и n: ";
    std::cin >> m >> n;
    std::cout << "A(" << m << ", " << n << ") = " << ackermann(m, n) << std::endl;
    return 0;
}

